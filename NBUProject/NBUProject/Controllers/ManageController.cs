        // POST: /Manage/ChangePassword
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> ChangePassword(ChangePasswordViewModel model)
        {
            if (string.IsNullOrEmpty(model.OldPassword) || string.IsNullOrEmpty(model.NewPassword) || string.IsNullOrEmpty(model.ConfirmPassword))
            {
                return PartialView("_ChangePasswordPartial", model);
            }
            if (model.OldPassword == model.NewPassword)
            {
                TempData["changePassword"] = "The new password and old password have to be different.";
                return PartialView("_ChangePasswordPartial", model);
            }
            var result = await UserManager.ChangePasswordAsync(User.Identity.GetUserId(), model.OldPassword, model.NewPassword);
            if (result.Succeeded)
            {
                var user = await UserManager.FindByIdAsync(User.Identity.GetUserId());
                if (user != null)
                {
                    await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);
                }
                return RedirectToAction("IndexPartialManage", new { Message = ManageMessageId.ChangePasswordSuccess });
            }
            AddErrors(result);
            return PartialView("_ChangePasswordPartial", model);
        }

