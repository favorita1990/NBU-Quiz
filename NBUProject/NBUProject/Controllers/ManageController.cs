        // POST: User/UserModels/Delete/5
        [HttpPost, ActionName("ModifyAccount")]
        public ActionResult ModifyConfirmed(UserModel model)
        {

            if((model.FirstName == null) && (model.LastName == null))
            {
                TempData["modifyFirstName"] = "Input first name.";
                TempData["modifyLastName"] = "Input last name.";
                return View("ModifyAccount", model);
            }
            if (model.FirstName == null)
            {
                TempData["modifyFirstName"] = "Input first name.";
                return View("ModifyAccount", model);
            }
            if (model.LastName == null)
            {
                TempData["modifyLastName"] = "Input last name.";
                return View("ModifyAccount", model);
            }

            DBContext db = new DBContext();
            var userStore = new UserStore<UserModel>(db);
            var UserManager = new UserManager<UserModel>(userStore);
            var userModel = db.Users.Find(User.Identity.GetUserId());

            string firstName = string.Format(
            "{0}{1}",
            model.FirstName.Trim().Substring(0, 1).ToUpper(),
            model.FirstName.Trim().Substring(1));

            string lastName = string.Format(
            "{0}{1}",
            model.LastName.Trim().Substring(0, 1).ToUpper(),
            model.LastName.Trim().Substring(1));

            userModel.FirstName = firstName;
            userModel.LastName = lastName;
            var entry = db.Entry(userModel);
            entry.State = EntityState.Modified;
            entry.Property(e => e.PasswordHash).IsModified = false;
            entry.Property(e => e.Gender).IsModified = false;
            entry.Property(e => e.Email).IsModified = false;
            entry.Property(e => e.UserName).IsModified = false;
            entry.Property(e => e.CreationDate).IsModified = false;
            entry.Property(e => e.SecurityStamp).IsModified = false;
            entry.Property(e => e.EmailConfirmed).IsModified = false;
            entry.Property(e => e.LockoutEnabled).IsModified = false;
            db.SaveChanges();


            return RedirectToAction("Change", "Manage", new { Message = ManageMessageId.ChangeNameSuccess });
        }


